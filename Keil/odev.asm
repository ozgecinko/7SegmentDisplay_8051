	; 7 Segment Display'de, P1.0'a bagli butona basinca OC2A yazan program.
	; Yazar: Özge Çinko.
	ORG 0H
	SJMP BASLA	
	ORG 30H		
BASLA:
	MOV A, #00001001B
	MOV DPTR, #YAZ
	CLR A
	MOVC A, @A+DPTR
	SETB P1.0
YAK: JB P1.0, YAK
	ACALL YAZ
	SJMP BASLA	
SONDUR: 
	JNB P1.0, SONDUR 
	SJMP BASLA
YAZ:
	MOV P2, #ISIM
	MOV P2, #SOYISIM
	MOV P2, #OGRETIM
	MOV P2, #SUBE 
ISIM: 
	 CLR P3.4
	 MOV P2, #11011100B
	 ACALL DELAY
	 CPL P3.4
SOYISIM: 
	CLR P3.5
	MOV P2, #11011000B
	ACALL DELAY
	CPL P3.5
OGRETIM:
	CLR P3.6
	MOV P2, #5BH
	ACALL DELAY
	CPL P3.6
SUBE: 
	CLR P3.7
	MOV P2, #11011111B
	ACALL DELAY
	CPL P3.7
DELAY:
	MOV TMOD,#01H
	MOV TH0,#0FCH
	MOV TL0,#018H
	SETB TR0
INTERRUPT:
	SETB TR0
	ACALL WAIT
	RETI
WAIT:MOV R4, #50h
D1: MOV R3, #20h
D2: MOV R2, #10h
D3: NOP
	DJNZ R2, D3
	DJNZ R3, D2
	DJNZ R4, D1
	RET
HERE:JNB TF0,HERE // Loops here until TF0 is set (ie;until roll over)
    CLR TR0 // Stops Timer 0
    CLR TF0 // Clears TF0 flag
    RET	
	END